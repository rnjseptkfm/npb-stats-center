export function translateNameToKorean(kana: string): string {
  let result = '';
  let i = 0;
  while (i < kana.length) {
    const doubleChar = kana.substring(i, i + 2);
    if (kanaToHangulMap[doubleChar]) {
      result += kanaToHangulMap[doubleChar];
      i += 2;
    } else {
      const singleChar = kana.charAt(i);
      if (singleChar === 'ッ') {
        const nextChar = kana.charAt(i + 1);
        if (nextChar && ['カ', 'キ', '쿠', '케', '코'].includes(nextChar)) result += ''; // Simplified
        result += ''; // Skip for now or handle batchim
      } else {
        result += kanaToHangulMap[singleChar] || singleChar;
      }
      i++;
    }
  }
  return result;
}
